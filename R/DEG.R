             (!!paste0(gene07, ":", "\n", gene01)),
             (!!paste0(gene07, ":", "\n", gene02)),
             (!!paste0(gene09, ":", "\n", gene05)),
             (!!paste0(gene10, ":", "\n", gene05)),
             (!!paste0(gene09, ":", "\n", gene06)),
             (!!paste0(gene10, ":", "\n", gene06)),
             (!!paste0(gene01, ":", "\n", gene04)),
             (!!paste0(gene02, ":", "\n", gene01)),
             (!!paste0(gene06, ":", "\n", gene05)),
