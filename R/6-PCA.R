    res.pca = PCA(df[[1]], # remove column with characters
                  scale.unit = TRUE,
                  ncp = 10,
                  graph = FALSE),
    df = df[[2]],
    x = "All",
    y = "sample.type",
    color = c("#D55E00", "#009E73", "#CC79A7", "#0072B2"),
    folder = "All"
  )

  .selected_biplot(
    res.pca = PCA(df[[1]], # remove column with characters
